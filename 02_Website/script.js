const storyStates = [
  ["working", "01", "story.workingTitle", "story.workingBody", "25%"],
  ["turn", "02", "story.turnTitle", "story.turnBody", "50%"],
  ["stalled", "03", "story.stalledTitle", "story.stalledBody", "75%"],
  ["resume", "04", "story.resumeTitle", "story.resumeBody", "100%"]
];

const lang = document.documentElement.dataset.lang === "zh" ? "zh" : "en";
const copy = dictionary[lang];

function applyCopy() {
  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const value = copy[node.dataset.i18n];
    if (value) node.textContent = value;
  });
}

function setStoryState(index) {
  const [key, step, title, body, progressValue] = storyStates[index];
  const panel = document.querySelector("[data-stage-panel]");
  const progress = document.querySelector("[data-stage-progress]");
  if (!panel || !progress) return;

  panel.querySelector(".stage-index").textContent = step;
  panel.querySelector("h3").textContent = copy[title];
  panel.querySelector("p").textContent = copy[body];
  progress.style.width = progressValue;

  document.querySelectorAll(".state-step").forEach((button) => {
    button.classList.toggle("is-active", button.dataset.state === key);
  });
}

function wireStateButtons() {
  document.querySelectorAll(".state-step").forEach((button, index) => {
    button.addEventListener("click", () => setStoryState(index));
  });
}

function wireScrollStory() {
  const story = document.querySelector(".scroll-story");
  if (!story) return;

  const onScroll = () => {
    const rect = story.getBoundingClientRect();
    const distance = Math.max(1, rect.height - window.innerHeight);
    const ratio = Math.min(1, Math.max(0, -rect.top / distance));
    const index = Math.min(storyStates.length - 1, Math.floor(ratio * storyStates.length));
    setStoryState(index);
  };

  window.addEventListener("scroll", onScroll, { passive: true });
  onScroll();
}

function wireCopyButton() {
  const button = document.querySelector("[data-copy-command]");
  const command = document.querySelector("[data-command]");
  if (!button || !command) return;

  button.addEventListener("click", async () => {
    try {
      await navigator.clipboard.writeText(command.textContent.trim());
      button.textContent = copy["install.copied"];
      window.setTimeout(() => {
        button.textContent = copy["install.copy"];
      }, 1400);
    } catch {
      button.textContent = copy["install.copy"];
    }
  });
}

applyCopy();
wireStateButtons();
wireScrollStory();
wireCopyButton();
